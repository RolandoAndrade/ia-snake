const SURVIVOR_RATE = 0.2;
const POPULATION = 80;
const MUTATION_RATE = 0.4;
const REPRODUCE_RATE = 0.10;
function cloneObject(src)
{
    return JSON.parse(JSON.stringify(src));
}



class Generation
{
    constructor(boards)
    {
        this.generation = 1;
        this.SURVIVORS = SURVIVOR_RATE*POPULATION;
        this.TO_REPRODUCE = REPRODUCE_RATE*POPULATION;
        this.boards = boards;
    }

    kill()
    {
        this.boards.sort(function(a, b) {return b.snake.getValoration() - a.snake.getValoration()});
    }


    procreate()
    {
        let baby = new Snake();
        let brain = new NeuronalNetwork(TOPOLOGY,new Sigmoid());
        let father = this.boards[Math.floor(Math.random() * this.TO_REPRODUCE)].snake;
        let mother = this.boards[Math.floor(Math.random() * this.TO_REPRODUCE)].snake;
        let layers = [];

        father.brain.layers.forEach(e=>
        {
            let layer = new Layer(0,0,e.activationFunction);
            let data = cloneObject(e);
            layer.W = data.W;
            layer.b = data.b;
            layers.push(layer);
        });

        layers.forEach((l,i)=>
        {


            l.W.forEach((r,j)=>
            {
               r.forEach((c,k)=>
               {
                    if(Math.random()<0.5)
                        layers[i].W[j][k]=mother.brain.layers[i].W[j][k];
               });
            });

            l.b.forEach((r,j)=>
            {
                if(Math.random()<0.5)
                    layers[i].b[j][0]=mother.brain.layers[i].b[j][0];
            });
        });
        brain.layers = layers;
        baby.brain = this.mutate(brain);
        return baby;
    }

    mutate(brain)
    {
        if(Math.random() < MUTATION_RATE)
        {
            brain.mutate();
        }
        return brain;
    }


    nextGeneration()
    {
        this.kill();
        for(let i = this.SURVIVORS; i < POPULATION; i++)
        {
            let baby = this.procreate();
            baby.x = this.boards[i].snake.x;
            baby.y = this.boards[i].snake.y;
            this.boards[i].snake = baby;
        }
        this.boards.forEach((e,i)=>
        {

            if(i===0)
            {
                e.snake.reset("#eeff7c");
            }
            else if(i<this.TO_REPRODUCE)
            {
                e.snake.reset("#f684ff")
            }
            else if(i<this.SURVIVORS)
            {
                e.snake.reset("#89ff90");
            }
            else
            {
                e.snake.reset();
            }
        });
        this.generation++;
    }
}

